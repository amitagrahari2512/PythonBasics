import math
import math as m
from math import sqrt,pow

x = math.sqrt(25)
print(x)

x = math.floor(2.9)
print(x)

x = math.ceil(2.1)
print(x)

x = math.pow(3,2)
print(x)

print("Constants values in Math import")
x = math.pi
print(x)

x = math.e
print(x)

print("We can use import alias name also : import math as m")
x = m.sqrt(25)
print(x)

print("We can import some functions as use : from math import sqrt,pow")
x = sqrt(25)
print(x)

x = pow(3,2)
print(x)

print("We can use help(math) function to find all method in it.")
help(math)



