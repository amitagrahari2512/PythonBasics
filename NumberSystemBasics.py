print("Number System")

print("Binary")
print("In python 0b starting from number act as binary")
print("decimal to Binary")
print("5 to binary : bin(5)",bin(5))

print("binary to decimal")
print("binary no 0b0101 in decimal ", 0b0101)

print("Octal")
print("In python 0o starting from number act as octal")
print("decimal to octal")
print("25 to octal : oct(25)",oct(25))

print("octal to decimal")
print("octal no 0o31 in decimal ", 0o31)

print("Hexadecimal")
print("In python 0x starting from number act as hexadecimal")
print("decimal to hexadecimal")
print("25 to hexadecimal : hex(25)",hex(25))
print("10 to hexadecimal : hex(10)",hex(10))
print("hexadecimal to decimal")
print("hexadecimal no 0xa in decimal ", 0xa)